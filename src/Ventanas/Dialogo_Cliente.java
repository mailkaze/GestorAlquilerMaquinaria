    private void cmdFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFotoActionPerformed
        JFileChooser j = new JFileChooser();
        j.showOpenDialog(null); //Abrimos un dialogo de selección de archivo
        File archivo = j.getSelectedFile();
        Clases.General.rutaImagen = archivo.getAbsolutePath(); //cargamos en 'ruta' la ruta de la imagen.
        ImageIcon icon = new ImageIcon(Clases.General.rutaImagen); 
        //redimensionamos la foto
        Image mini = icon.getImage().getScaledInstance(100, 100, Image.SCALE_AREA_AVERAGING);
        Foto.setIcon(new ImageIcon(mini));
    }//GEN-LAST:event_cmdFotoActionPerformed

    private void FotoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FotoMouseClicked
        Desktop desktop = Desktop.getDesktop(); //Instanciamos la clase Desktop
        String ruta="img/"+Clases.Clientes.ID_Cliente+".jpg"; //Le damos la ruta del archivo.
        File archivo = new File(ruta); //Instanciamos la clase File con esa ruta.+
        if (archivo.exists()){
            try {
                desktop.open(archivo); //el método open de la clase Desktop llama al programa prederminado que abre ese archivo.
            } catch (IOException ex) {
                Logger.getLogger(Dialogo_Cliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_FotoMouseClicked

