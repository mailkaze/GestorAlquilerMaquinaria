    public void cargarDatos(){
        if (Clases.General.CargarDatos==true){
            txtNombre.setText(Clases.Clientes.CNombre);
            txtApellidos.setText(Clases.Clientes.CApellidos);
            txtCI.setText(Clases.Clientes.CCI);
            txtNIT.setText(Clases.Clientes.CNIT);
            txtTelfCasa.setText(Clases.Clientes.CTelfCasa);
            txtTelfEmp.setText(Clases.Clientes.CTelfEmp);
            txtCelular.setText(Clases.Clientes.CCel);
            txtCelular2.setText(Clases.Clientes.CCel2);
            txtDireccion.setText(Clases.Clientes.CDireccion);
            if (Clases.Clientes.CLuz==true)chkLuz.setSelected(true);
            else chkLuz.setSelected(false);
            if (Clases.Clientes.CAgua==true)chkAgua.setSelected(true);
            else chkAgua.setSelected(false);
            txtEmpresa.setText(Clases.Clientes.CEmpresa);
            txtEmail.setText(Clases.Clientes.CEmail);
            txtProfesion.setText(Clases.Clientes.CProfesion);
            if (Clases.Clientes.CNoGrato==true)chkNoGrato.setSelected(true);
            else chkNoGrato.setSelected(false);
            cmdEliminar.setEnabled(true);
            mnuEliminar.setEnabled(true);
            mnuModificar.setEnabled(true);
            habilitar=false;
            //Creamos un File con la ruta de la foto que necesitamos
            File archivo = new File("img/"+Clases.Clientes.ID_Cliente+".jpg");
            if (archivo.exists()){//si el archivo con esa ruta existe cargamos la foto
                ImageIcon icon = new ImageIcon("img/"+Clases.Clientes.ID_Cliente+".jpg");
                //redimensionamos la foto
                Image mini = icon.getImage().getScaledInstance(100, 100, Image.SCALE_AREA_AVERAGING);
                Foto.setIcon(new ImageIcon(mini));
            }else{//Sino, cargamos la imagen por defecto.
                Foto.setIcon(new ImageIcon(getClass().getResource("/Recursos/none.jpg")));
            }
        }
        else{
            cmdEliminar.setEnabled(false);
            mnuEliminar.setEnabled(false);
            mnuModificar.setEnabled(false);
            habilitar=true;
            Clases.Clientes.ID_Cliente=0;
            Foto.setIcon(new ImageIcon(getClass().getResource("/Recursos/none.jpg")));
        }
    }
